
package mythirdprogram;
import java.util.Scanner;
public class MyThirdProgram 
{

    public static void main(String[] args) 
    {
        int x = 10;
        int y = 20;
        double z;
        z = x + y;
        System.out.println("The sum of 10 and 20 = " + z);
        z = x - y;
        System.out.println("The difference of 10 and 20 = " + z);
        z = (double)x / y;
        System.out.println("The quotient of 10 and 20 = " + z);
        z = x * y;
        System.out.println("The product of 10 and 20 = " + z);
        z = x % y;
        System.out.println("The modulus of 10 and 20 = " + z);
    }
        
}
